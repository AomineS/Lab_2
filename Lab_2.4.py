# Дано три різних числа. Знайти, яке з них є середнім (більше одного, але менше іншого)
a = int(input("Число 1 = "))
b = int(input("Число 2 = "))
c = int(input("Число 3 = "))
if b < a < c or c < a < b:
    print("Середнє число: ", a)
elif a < b < c or c < b < a:
    print("Середнє число: ", b)
else:
    print("Середнє число: ", c)
